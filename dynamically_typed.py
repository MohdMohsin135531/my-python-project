# Python is dynamically typed meaning that only when the program is run will the type of a variable be determined. The type belongs to the value -> not the variable.

v = 10  # variable v stores an integer value

v = 'hello'  # v now stores a string value

print(v)

# Python is also strongly typed meaning that the type of a variable is always known and cannot be changed.

print(125 + 125)  #  250

print("125" + "125")  # "125125"

print(125 * 4)  # 500

print("125" * 4)  # "125125125125"

print("This is a number:", 125)  # The print() function will print all the arguments delimited by a space.
